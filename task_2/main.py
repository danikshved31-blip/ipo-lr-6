import random  # Импортируем модуль для работы со случайными числами
numbers = [-3, -5, -2, -12, 0, 15, 4, 7, 2]  # Список возможных чисел для матрицы
stroki = random.randint(4, 8)  # Генерируем случайное количество строк (от 4 до 8)
stolbiki = random.randint(4, 8)  # Генерируем случайное количество столбцов (от 4 до 8)
summa = 0  # Инициализируем переменную для суммы отрицательных чисел
for i in range(stolbiki):  # Цикл по количеству столбцов (создаем строки матрицы)
    i = random.choices(numbers, k = stroki)  # Создаем одну строку матрицы со случайными числами
    for num in i:  # Цикл по каждому числу в текущей строке
        if num < 0:  # Проверяем, является ли число отрицательным
            summa += num  # Если число отрицательное, добавляем его к сумме
        print(f"{num:3}", end="")  # Выводим число, занимающее 3 символа, без перевода строки
    print()  # Переходим на новую строку после вывода всей строки матрицы
print(summa)  # Выводим общую сумму всех отрицательных чисел
